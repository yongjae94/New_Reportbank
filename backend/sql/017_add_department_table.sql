VALUES (SYS_GUID(), 'fin', '재무', 'Y');
INSERT INTO RPT.TB_RPT_DEPT (DEPT_ID, DEPT_CODE, DEPT_NAME, USE_YN)
VALUES (SYS_GUID(), 'hr', '인사', 'Y');
INSERT INTO RPT.TB_RPT_DEPT (DEPT_ID, DEPT_CODE, DEPT_NAME, USE_YN)
VALUES (SYS_GUID(), 'sec', '정보보호', 'Y');

ALTER TABLE RPT.TB_RPT_APP_USER ADD (DEPT_ID VARCHAR2(36));

UPDATE RPT.TB_RPT_APP_USER u
SET DEPT_ID = (
    SELECT d.DEPT_ID FROM RPT.TB_RPT_DEPT d
    WHERE d.DEPT_CODE = LOWER(TRIM(u.TEAM_ID)) AND ROWNUM = 1
)
WHERE u.DEPT_ID IS NULL AND u.TEAM_ID IS NOT NULL;

UPDATE RPT.TB_RPT_APP_USER u
SET DEPT_ID = (SELECT MIN(d.DEPT_ID) FROM RPT.TB_RPT_DEPT d WHERE d.DEPT_CODE = 'platform')
WHERE u.DEPT_ID IS NULL;

ALTER TABLE RPT.TB_RPT_APP_USER
    ADD CONSTRAINT FK_TB_RPT_APP_USER_DEPT FOREIGN KEY (DEPT_ID)
    REFERENCES RPT.TB_RPT_DEPT (DEPT_ID);

CREATE INDEX RPT.IX_TB_RPT_APP_USER_DEPT ON RPT.TB_RPT_APP_USER (DEPT_ID)
TABLESPACE TS_RPT_DAT;

COMMIT;
