-- 부서 마스터 + APP 사용자 DEPT_ID (이름 변경은 DEPT_NAME만 수정).
-- RPT 소유자로 실행.

CREATE TABLE RPT.TB_RPT_DEPT (
    DEPT_ID         VARCHAR2(36)       NOT NULL,
    DEPT_CODE       VARCHAR2(32)       NOT NULL,
    DEPT_NAME       VARCHAR2(128)      NOT NULL,
    USE_YN          CHAR(1)            DEFAULT 'Y' NOT NULL,
    CREATED_AT      TIMESTAMP(6) WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT      TIMESTAMP(6) WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_TB_RPT_DEPT PRIMARY KEY (DEPT_ID),
    CONSTRAINT UQ_TB_RPT_DEPT_CODE UNIQUE (DEPT_CODE),
    CONSTRAINT CK_TB_RPT_DEPT_USEYN CHECK (USE_YN IN ('Y','N'))
)
TABLESPACE TS_RPT_DAT;

CREATE OR REPLACE TRIGGER RPT.TRG_TB_RPT_DEPT_UPD
BEFORE UPDATE ON RPT.TB_RPT_DEPT
FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSTIMESTAMP;
END;
/

INSERT INTO RPT.TB_RPT_DEPT (DEPT_ID, DEPT_CODE, DEPT_NAME, USE_YN)
VALUES (SYS_GUID(), 'platform', '플랫폼', 'Y');
