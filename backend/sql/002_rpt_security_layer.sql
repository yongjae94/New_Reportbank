-- 5) update trigger
CREATE OR REPLACE TRIGGER RPT.TRG_TB_RPT_PII_METADATA_UPD
BEFORE UPDATE ON RPT.TB_RPT_PII_METADATA
FOR EACH ROW
BEGIN
    :NEW.UPDATED_AT := SYSTIMESTAMP;
END;
