-- RPT security layer objects
-- Naming rule:
--   TABLE : TB_RPT_*
--   FUNC  : FN_RPT_*

-- 1) PII metadata table
CREATE TABLE RPT.TB_RPT_PII_METADATA (
    META_ID            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TARGET_OWNER       VARCHAR2(128)      NOT NULL,
    TARGET_TABLE       VARCHAR2(128)      NOT NULL,
    TARGET_COLUMN      VARCHAR2(128)      NOT NULL,
    MASKING_TYPE       VARCHAR2(32)       NOT NULL, -- RRN | PHONE | NAME | CUSTOM
    USE_YN             CHAR(1)            DEFAULT 'Y' NOT NULL,
    CREATED_AT         TIMESTAMP(6) WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT         TIMESTAMP(6) WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT CK_TB_RPT_PII_META_USE_YN CHECK (USE_YN IN ('Y', 'N'))
);

CREATE UNIQUE INDEX RPT.UIX_TB_RPT_PII_METADATA_COL
    ON RPT.TB_RPT_PII_METADATA (TARGET_OWNER, TARGET_TABLE, TARGET_COLUMN);

CREATE INDEX RPT.IX_TB_RPT_PII_METADATA_TABLE
    ON RPT.TB_RPT_PII_METADATA (TARGET_OWNER, TARGET_TABLE, USE_YN);

-- 2) Audit trail table
CREATE TABLE RPT.TB_RPT_AUDIT_LOG (
    AUDIT_ID            VARCHAR2(36)       NOT NULL,
    USER_ID             VARCHAR2(128)      NOT NULL,
    TEAM_ID             VARCHAR2(128)      NOT NULL,
    ACTION_TYPE         VARCHAR2(32)       NOT NULL, -- PSR_REALTIME_QUERY | REPORT_EXECUTE
    TARGET_TYPE         VARCHAR2(32)       NOT NULL, -- PSR | REPORT
    TARGET_ID           VARCHAR2(128)      NOT NULL, -- PSR_NO or REPORT_ID
    REQUESTED_SQL       CLOB               NOT NULL,
    ROW_COUNT           NUMBER             DEFAULT 0 NOT NULL,
    USED_UNMASK         CHAR(1)            DEFAULT 'N' NOT NULL,
    ACCESSED_AT         TIMESTAMP(6) WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CLIENT_IP           VARCHAR2(64),
    CONSTRAINT PK_TB_RPT_AUDIT_LOG PRIMARY KEY (AUDIT_ID),
    CONSTRAINT CK_TB_RPT_AUDIT_UNMASK CHECK (USED_UNMASK IN ('Y', 'N'))
);

CREATE INDEX RPT.IX_TB_RPT_AUDIT_LOG_USER_TIME
    ON RPT.TB_RPT_AUDIT_LOG (USER_ID, ACCESSED_AT);

CREATE INDEX RPT.IX_TB_RPT_AUDIT_LOG_TARGET
    ON RPT.TB_RPT_AUDIT_LOG (TARGET_TYPE, TARGET_ID, ACCESSED_AT);

-- 3) Report template master table
CREATE TABLE RPT.TB_RPT_REPORT_MST (
    REPORT_ID           VARCHAR2(128)      NOT NULL,
    REPORT_TITLE        VARCHAR2(256)      NOT NULL,
    REPORT_DESC         VARCHAR2(1000),
    SQL_TEMPLATE        CLOB               NOT NULL,
    ALLOWED_DEPTS_JSON  CLOB               NOT NULL, -- JSON array: ["FIN","HR"]
    IS_PUBLIC_YN        CHAR(1)            DEFAULT 'Y' NOT NULL, -- Y=approved/public, N=locked
    LAST_MODIFIED_BY    VARCHAR2(128),
    LAST_MODIFIED_AT    TIMESTAMP(6) WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CREATED_AT          TIMESTAMP(6) WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_TB_RPT_REPORT_MST PRIMARY KEY (REPORT_ID),
    CONSTRAINT CK_TB_RPT_REPORT_MST_PUBLIC CHECK (IS_PUBLIC_YN IN ('Y', 'N'))
);

CREATE INDEX RPT.IX_TB_RPT_REPORT_MST_PUBLIC
    ON RPT.TB_RPT_REPORT_MST (IS_PUBLIC_YN, LAST_MODIFIED_AT);

-- 4) Common masking function
CREATE OR REPLACE FUNCTION RPT.FN_RPT_GET_MASKED_VALUE (
    P_VALUE IN VARCHAR2,
    P_MASKING_TYPE IN VARCHAR2
) RETURN VARCHAR2
IS
    V_VAL   VARCHAR2(4000) := NVL(P_VALUE, '');
    V_TYPE  VARCHAR2(32)   := UPPER(NVL(P_MASKING_TYPE, ''));
BEGIN
    IF V_VAL IS NULL OR LENGTH(V_VAL) = 0 THEN
        RETURN V_VAL;
    END IF;

    IF V_TYPE = 'RRN' THEN
        -- 900101-1234567 -> 900101-1******
        IF REGEXP_LIKE(V_VAL, '^[0-9]{6}-?[0-9]{7}$') THEN
            RETURN REGEXP_REPLACE(V_VAL, '^([0-9]{6})-?([0-9]).*$', '\1-\2******');
        END IF;
        RETURN SUBSTR(V_VAL, 1, 8) || '******';
    ELSIF V_TYPE = 'PHONE' THEN
        -- 01012341234 / 010-1234-1234 -> 010-****-1234
        IF REGEXP_LIKE(V_VAL, '^01[0-9]-?[0-9]{3,4}-?[0-9]{4}$') THEN
            RETURN REGEXP_REPLACE(V_VAL, '^(01[0-9])[-]?([0-9]{3,4})[-]?([0-9]{4})$', '\1-****-\3');
        END IF;
        RETURN SUBSTR(V_VAL, 1, 3) || '-****-' || SUBSTR(V_VAL, GREATEST(LENGTH(V_VAL)-3, 1), 4);
    ELSIF V_TYPE = 'NAME' THEN
        -- 홍길동 -> 홍*동
        IF LENGTH(V_VAL) >= 3 THEN
            RETURN SUBSTR(V_VAL, 1, 1) || RPAD('*', LENGTH(V_VAL)-2, '*') || SUBSTR(V_VAL, -1, 1);
        ELSIF LENGTH(V_VAL) = 2 THEN
            RETURN SUBSTR(V_VAL, 1, 1) || '*';
        END IF;
        RETURN '*';
    END IF;

    RETURN V_VAL;
END;
