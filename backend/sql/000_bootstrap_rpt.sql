--------------------------------------------------------------------------------
-- 3) PII reference view (bootstrap version)
--    - Keeps backend contract: OWNER, TABLE_NAME, COLUMN_NAME, PII_YN
--    - Can later be replaced by enterprise metadata view source
--------------------------------------------------------------------------------
CREATE OR REPLACE VIEW RPT.VW_PII_META AS
SELECT
    TARGET_OWNER AS OWNER,
    TARGET_TABLE AS TABLE_NAME,
    TARGET_COLUMN AS COLUMN_NAME,
    CASE WHEN USE_YN = 'Y' THEN 'Y' ELSE 'N' END AS PII_YN
FROM RPT.TB_RPT_PII_METADATA;

--------------------------------------------------------------------------------
-- 4) Masking function
--------------------------------------------------------------------------------
CREATE OR REPLACE FUNCTION RPT.FN_RPT_GET_MASKED_VALUE (
    P_VALUE IN VARCHAR2,
    P_MASKING_TYPE IN VARCHAR2
) RETURN VARCHAR2
IS
    V_VAL   VARCHAR2(4000) := NVL(P_VALUE, '');
    V_TYPE  VARCHAR2(32)   := UPPER(NVL(P_MASKING_TYPE, ''));
BEGIN
    IF V_VAL IS NULL OR LENGTH(V_VAL) = 0 THEN
        RETURN V_VAL;
    END IF;

    IF V_TYPE = 'RRN' THEN
        IF REGEXP_LIKE(V_VAL, '^[0-9]{6}-?[0-9]{7}$') THEN
            RETURN REGEXP_REPLACE(V_VAL, '^([0-9]{6})-?([0-9]).*$', '\1-\2******');
        END IF;
        RETURN SUBSTR(V_VAL, 1, 8) || '******';
    ELSIF V_TYPE = 'PHONE' THEN
        IF REGEXP_LIKE(V_VAL, '^01[0-9]-?[0-9]{3,4}-?[0-9]{4}$') THEN
            RETURN REGEXP_REPLACE(V_VAL, '^(01[0-9])[-]?([0-9]{3,4})[-]?([0-9]{4})$', '\1-****-\3');
        END IF;
        RETURN SUBSTR(V_VAL, 1, 3) || '-****-' || SUBSTR(V_VAL, GREATEST(LENGTH(V_VAL)-3, 1), 4);
    ELSIF V_TYPE = 'NAME' THEN
        IF LENGTH(V_VAL) >= 3 THEN
            RETURN SUBSTR(V_VAL, 1, 1) || RPAD('*', LENGTH(V_VAL)-2, '*') || SUBSTR(V_VAL, -1, 1);
        ELSIF LENGTH(V_VAL) = 2 THEN
            RETURN SUBSTR(V_VAL, 1, 1) || '*';
        END IF;
        RETURN '*';
    END IF;

    RETURN V_VAL;
END;
/
